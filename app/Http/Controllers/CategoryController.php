    public function getCategories()
    {
        $categories = Language::where('lang_code', 'en')
        ->join('categories as CT', 'CT.lang_id', '=', 'languages.id')
        ->where('CT.status', 1)
        ->select('CT.id', 'CT.parent_id', 'CT.name', 'CT.slug', 'CT.status')
        ->get();

